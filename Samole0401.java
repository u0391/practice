package rensyuuproject;

public class Samole0401 {

	public static void main(String[] args) {

		int x =80;
		
		if(x>=80) {
			
			System.out.println(x+"点は「優」です。");
			
		}else if(x>=60) {
			
			System.out.println(x+"点は「良」です。");
			
		}else if(x>=40) {
			
			System.out.println(x+"点は「可」です。");
			
		}else {
			
			System.out.println(x+"点は「不可」です。");
		}

	}

}
