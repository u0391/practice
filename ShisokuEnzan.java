package rensyuuproject;

public class ShisokuEnzan {

	public static void main(String[] args) {

		int a =9;
		
		int b =4;
		
		int answer;
		
		System.out.print("a =");
		
		System.out.println(a);
		
		System.out.print("b =");
		
		System.out.println(b);
		
		//足し算
		answer = a+ b;
		
		System.out.print("a+b :");
		
		System.out.println(answer);
		
		//引き算
		
		answer = a-b;
		
		System.out.print("a-b :");
		
		System.out.println(answer);
		
		//掛け算
		
		answer = a*b;
		
		System.out.print("a*b :");
		
		System.out.println(answer);
		
		//割り算
		answer = a/b;
		
		System.out.println("a/b :");
		
		System.out.println(answer);
		
		//余りを求める
		answer = a%b;
		
		System.out.print("a%b :");
		
		System.out.println(answer);

	}

}
